package com.oopexamples;

public class Employee {
    // Private instance variables (data hiding)
    private String name;
    private int id;

    // Public getter and setter methods to access variables
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
